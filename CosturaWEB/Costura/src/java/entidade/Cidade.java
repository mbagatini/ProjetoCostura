package entidade;

/**
 *
 * @author Morgana
 */
public class Cidade {
    
    private int codigo;
    private String nome;
    private Estado estado;
    
    public Cidade(){
        codigo = 0;
        nome = "";
        estado = new Estado();
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Estado getEstado() {
        return estado;
    }

    public void setEstado(Estado estado) {
        this.estado = estado;
    }
    
    
    
}
